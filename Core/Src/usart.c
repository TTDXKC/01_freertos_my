// Send 16bit
void Usart_SendHalfWord(UART_HandleTypeDef *huart, uint16_t ch)
{
    uint8_t temp[2];
    
    temp[0] = (ch >> 8) & 0xFF;  // �߰�λ
    temp[1] = ch & 0xFF;         // �Ͱ�λ
    
    HAL_UART_Transmit(huart, temp, 2, 0xFFFF);
}

// Send 1 Byte
void Usart_SendByte(UART_HandleTypeDef *huart, uint8_t ch)
{
    HAL_UART_Transmit(huart, &ch, 1, 0xFFFF);
}

// Send len string
void Usart_SendString(UART_HandleTypeDef *huart, unsigned char *str, unsigned short len)
{
    HAL_UART_Transmit(huart, str, len, 0xFFFF);
}

// Send String  which '\0' end
void Usart_SendString2(UART_HandleTypeDef *huart, char *str)
{
    HAL_UART_Transmit(huart, (uint8_t*)str, strlen(str), 0xFFFF);
}

// Formatted out to uart
void UsartPrintf(UART_HandleTypeDef *huart, char *fmt, ...)
{
    unsigned char UsartPrintfBuf[296];
    va_list ap;
    
    va_start(ap, fmt);
    vsnprintf((char *)UsartPrintfBuf, sizeof(UsartPrintfBuf), fmt, ap);
    va_end(ap);
    
    HAL_UART_Transmit(huart, UsartPrintfBuf, strlen((char *)UsartPrintfBuf), 0xFFFF);
}


